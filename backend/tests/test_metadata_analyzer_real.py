class TestAISignatureDetection:
    """Test AI signature detection in metadata."""

    @pytest.mark.parametrize("tool", ["sora", "runway", "pika", "kling", "veo"])
    def test_detects_known_ai_tool_in_encoder(self, mock_video_path, tool):
        """Should detect each known AI tool signature in encoder metadata."""
        analyzer = MetadataAnalyzer()
        metadata = make_ai_metadata(tool)

        score, findings = analyzer._check_ai_signatures(metadata)

        assert score >= 80.0, f"Tool '{tool}' should give high score, got {score}"
        sig_findings = [f for f in findings if f.type == "codec_signature"]
        assert len(sig_findings) > 0, f"No codec_signature finding for tool '{tool}'"

    def test_no_signature_for_real_camera(self, mock_video_path):
        """Real camera metadata should not trigger AI signature."""
        analyzer = MetadataAnalyzer()
        metadata = make_real_camera_metadata()

        score, findings = analyzer._check_ai_signatures(metadata)

        sig_findings = [f for f in findings if f.type == "codec_signature"]
        assert len(sig_findings) == 0, "Real camera should not trigger AI signature"

    def test_ai_metadata_tag_detected(self, mock_video_path):
        """Metadata with explicit ai_generated tag should be flagged."""
        analyzer = MetadataAnalyzer()
        metadata = make_minimal_metadata()
        metadata["format"]["tags"]["ai_generated"] = "true"

        score, findings = analyzer._check_ai_signatures(metadata)

        tag_findings = [f for f in findings if f.type == "ai_metadata_tag"]
        assert len(tag_findings) > 0


class TestCodecParameterAnalysis:
    """Test codec parameter anomaly detection."""

    def test_low_bitrate_flagged(self, mock_video_path):
        """Video bitrate < 200 kbps should trigger low_bitrate finding."""
