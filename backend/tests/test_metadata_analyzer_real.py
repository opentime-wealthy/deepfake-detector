class TestRealMetadataAnalyzer:
    """Unit tests for MetadataAnalyzer."""

    def test_analyze_returns_result(self, mock_video_path):
        """analyze() returns AnalyzerResult even when ffprobe fails."""
        analyzer = MetadataAnalyzer()
        with patch.object(analyzer, "_extract_metadata", return_value=make_minimal_metadata()):
            result = analyzer.analyze(mock_video_path)

        assert isinstance(result, AnalyzerResult)
        assert 0.0 <= result.score <= 100.0

    def test_ffprobe_unavailable_returns_error(self, mock_video_path):
        """analyze() returns error result when metadata extraction fails."""
        analyzer = MetadataAnalyzer()
        with patch.object(analyzer, "_extract_metadata", return_value=None):
            result = analyzer.analyze(mock_video_path)

        assert result.error is not None

    def test_score_range_valid(self, mock_video_path):
        """Score must always be in [0, 100]."""
        analyzer = MetadataAnalyzer()
        for metadata in [
            make_ai_metadata("sora"),
            make_real_camera_metadata(),
            make_minimal_metadata(),
        ]:
            with patch.object(analyzer, "_extract_metadata", return_value=metadata):
                result = analyzer.analyze(mock_video_path)
            assert 0.0 <= result.score <= 100.0


