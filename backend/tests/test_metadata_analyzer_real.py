@pytest.mark.slow
class TestMetadataAnalyzerIntegration:
    """Integration tests that call ffprobe."""

    def test_real_file_with_ffprobe(self, tmp_path):
        """
        Metadata extraction from a real MP4 file.
        NOTE: Requires ffprobe/ffmpeg installed.
        """
        import subprocess
        # Create a minimal valid MP4 with ffmpeg
        out_path = str(tmp_path / "test.mp4")
        ret = subprocess.run(
            [
                "ffmpeg", "-f", "lavfi", "-i", "color=c=blue:s=320x240:d=2",
                "-c:v", "libx264", "-t", "2", out_path, "-y"
            ],
            capture_output=True,
        )
        if ret.returncode != 0:
            pytest.skip("ffmpeg not available")

        analyzer = MetadataAnalyzer()
        result = analyzer.analyze(out_path)

        assert isinstance(result, AnalyzerResult)
        assert 0.0 <= result.score <= 100.0
